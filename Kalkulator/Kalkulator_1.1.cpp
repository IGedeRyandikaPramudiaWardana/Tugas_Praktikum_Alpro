#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[])
{
	int x = atoi(argv[1]);
	int y = atoi(argv[3]);
	
	
	if(argv[2], "+")
	{
		printf("Hasil penjumlahan = %d", x + y);
	}
	
	else if(argv[2], "-")
	{
		printf("Hasil Pengurangan = %d", x - y);
	}
	
	return 0;
}